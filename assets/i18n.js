// Language detection
const LANG_MAP = {
  cs:'cs', sk:'cs', de:'de', fr:'fr', es:'es', ca:'es', it:'it', pl:'pl',
  ru:'ru', be:'ru', uk:'uk', ja:'ja', ko:'ko', 'zh-hans':'zh', 'zh-cn':'zh',
  'zh-tw':'zh', 'zh-hant':'zh', ar:'ar', hi:'hi', tr:'tr', 'pt-br':'pt',
  pt:'pt', id:'id', ms:'id', nl:'nl', sv:'sv', da:'sv', no:'sv',
  th:'th', vi:'vi', sw:'sw', ku:'ku', az:'az', fa:'fa',
  fil:'fil', tl:'fil',
};

function detectLang() {
  const saved = localStorage.getItem('mm_lang');
  if (saved && TRANSLATIONS[saved]) return saved;
  const langs = navigator.languages || [navigator.language || 'en'];
  for (const l of langs) {
    const key = l.toLowerCase();
    if (TRANSLATIONS[key]) return key;
    if (LANG_MAP[key]) return LANG_MAP[key];
    const base = key.split('-')[0];
    if (TRANSLATIONS[base]) return base;
    if (LANG_MAP[base]) return LANG_MAP[base];
  }
  return 'en';
}

function applyLang(lang) {
  const t = TRANSLATIONS[lang] || TRANSLATIONS['en'];
  document.querySelectorAll('[data-i18n]').forEach(el => {
    const key = el.dataset.i18n;
    if (t[key] !== undefined) el.innerHTML = t[key];
  });
  // RTL for Arabic and Persian
  document.documentElement.dir = (lang === 'ar' || lang === 'fa') ? 'rtl' : 'ltr';
  document.documentElement.lang = lang;
  localStorage.setItem('mm_lang', lang);
  // update active button
  document.querySelectorAll('.lang-btn').forEach(b => {
    b.classList.toggle('active', b.dataset.lang === lang);
  });
  // re-init leaderboard labels
  if (window._lbDiff !== undefined) loadLeaderboard(window._lbDiff);
}

window.setLang = function(lang) { applyLang(lang); };
document.addEventListener('DOMContentLoaded', () => applyLang(detectLang()));
